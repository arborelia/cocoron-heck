function makeName(seed) {
    var rng = seedrandom(seed);
    var nameChoice = Math.floor(rng() * NAMES.length);
    var vowelChoice = Math.floor(rng() * 5);
    var name = NAMES[nameChoice];
    for (const placeholder of ['1', '2', '3', '4', '5', '?']) {
        name = name.replace(placeholder, VOWELS[placeholder][vowelChoice]);
    }
    return name.toUpperCase();
}

